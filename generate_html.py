        nav_html = ""
        for item in nav_items:
            active_class = " active" if item["name"] == page_name else ""
            nav_html += f'            <li><a href="{item["name"]}" class="nav-link{active_class}"><i class="{item["icon"]}"></i> {item["title"]}</a></li>\n'
        
